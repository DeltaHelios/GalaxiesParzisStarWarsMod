	/**
	 * Sets the ARGB OpenGL color from a packed ARGB color int
	 *
	 * @param argb The color to use
	 */
	public static void glColorI(int argb)
	{
		int alpha = argb >> 24 & 0xFF;
		int red = argb >> 16 & 0xFF;
		int green = argb >> 8 & 0xFF;
		int blue = argb & 0xFF;
		GL11.glColor4f(red / 255f, green / 255f, blue / 255f, alpha / 255f);
	}

	public static void glColorI(int rgb, int a)
	{
		int alpha = a & 0xFF;
		int red = rgb >> 16 & 0xFF;
		int green = rgb >> 8 & 0xFF;
		int blue = rgb & 0xFF;
		GL11.glColor4f(red / 255f, green / 255f, blue / 255f, alpha / 255f);
	}

	/**
	 * Turns a packed RGB color into a Color
	 *
	 * @param rgb The color to unpack
	 * @return The new Color
	 */
	public static Color intToColor(int rgb)
	{
		int red = rgb >> 16 & 0xFF;
		int green = rgb >> 8 & 0xFF;
		int blue = rgb & 0xFF;
		return new Color(red, green, blue, 255);
	}

	/**
	 * Turns a Color into a packed RGB int
	 *
	 * @param c The color to pack
	 * @return The packed int
	 */
	public static int colorToInt(Color c)
	{
		int rgb = c.getRed();
		rgb = (rgb << 8) + c.getGreen();
		rgb = (rgb << 8) + c.getBlue();
		return rgb;
	}

	/**
	 * Makes an RGB packed int opaque in the A channel
	 *
	 * @param rgb The color to make opaque
	 * @return The opaque color
	 */
	public static int makeOpaque(int rgb)
	{
		return -16777216 | rgb;
	}

	public static void lerpColor3(float percent, Color ca, Color cb, Color cc)
	{
		if (percent < 0.5)
			lerpColor2(percent * 2, cb, ca);
		else
			lerpColor2((percent - 0.5) * 2, cc, cb);
	}

	public static void lerpColor2(double percent, Color ca, Color cb)
	{
		double v = 1 - percent;
		double r = ca.getRed() * percent + cb.getRed() * v;
		double g = ca.getGreen() * percent + cb.getGreen() * v;
		double b = ca.getBlue() * percent + cb.getBlue() * v;
		GL11.glColor4d(r / 255f, g / 255f, b / 255f, 1f);
	}

	public static void lerpColor3(float percent, int ca, int cb, int cc)
	{
		if (percent < 0.5)
			lerpColor2(percent * 2, cb, ca);
		else
			lerpColor2((percent - 0.5) * 2, cc, cb);
	}

	public static void lerpColor2(double percent, int ca, int cb)
	{
		double v = 1 - percent;

		int ared = ca >> 16 & 0xFF;
		int agreen = ca >> 8 & 0xFF;
		int ablue = ca & 0xFF;

		int bred = cb >> 16 & 0xFF;
		int bgreen = cb >> 8 & 0xFF;
		int bblue = cb & 0xFF;

		double r = ared * v + bred * percent;
		double g = agreen * v + bgreen * percent;
		double b = ablue * v + bblue * percent;
		GL11.glColor4d(r / 255f, g / 255f, b / 255f, 1f);
	}

	public static int getLerpColor2(double percent, int ca, int cb)
	{
		double v = 1 - percent;

		int ared = ca >> 16 & 0xFF;
		int agreen = ca >> 8 & 0xFF;
		int ablue = ca & 0xFF;

		int bred = cb >> 16 & 0xFF;
		int bgreen = cb >> 8 & 0xFF;
		int bblue = cb & 0xFF;

		double r = ared * v + bred * percent;
		double g = agreen * v + bgreen * percent;
		double b = ablue * v + bblue * percent;

		return Fx.Util.GetRgb((int)r, (int)g, (int)b);
	}
